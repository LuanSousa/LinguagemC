//Primeira coisa ao iniciar um programa em C é declarar as bibliotecas
#include <stdio.h> //Biblioteca para entrada e saída dedados
#include <stdlib.h>// Biblioteca auxiliar

//Segunda coisa a fazer é declarar a função principal(main) do nosso programa.
//Tudo que estiver dentro das chaves {},será executado.
int main()
{
    printf("Hello world!\n");//Comando para mostrar/printar uma mensagem na tela- printf
    return 0; //Caso o nosso programa execute sem problema algum, retona 0.
}
//Agora que ja aprendemos sobre a estrutura de um programa em C, podemos executá-lo
